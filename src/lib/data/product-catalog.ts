/** Normalize a product — fill legacy alias fields from source-of-truth fields */
function normalize(p: RawProduct): Product {
  return {
    ...p,
    costLevel: p.priceEstimate,
    implementationLevel: p.implementationDifficulty,
    maturityLevel: p.maturity,
    integrationRequired: p.integrationNeeded,
    ikerScore: p.recommendationScore,
    contractsWon: 0,
  };
}

export const PRODUCT_CATALOG: Product[] = RAW_CATALOG.map(normalize);

/** Safe category color lookup — returns cyan fallback for unknown categories */
export function categoryColor(cat: string): string {
  // Try exact match first (handles 'AI'), then lowercase match
  return CATEGORY_COLORS[cat as ProductCategory]
    ?? CATEGORY_COLORS[cat.toLowerCase() as ProductCategory]
    ?? '#00d4ff';
}

// ── Helper Functions ──────────────────────────────────────────────────────────

export function getProductById(id: string): Product | undefined {
  return PRODUCT_CATALOG.find(p => p.id === id);
}

export function getProductsByCompany(company: string): Product[] {
  return PRODUCT_CATALOG.filter(p =>
    p.company.toLowerCase().includes(company.toLowerCase())
  );
}

export function getProductsByIndustry(industry: string): Product[] {
  return PRODUCT_CATALOG.filter(p =>
    p.industries.some(i => i.toLowerCase().includes(industry.toLowerCase()))
  );
}

export function getProductsByCategory(category: string): Product[] {
  return PRODUCT_CATALOG.filter(p =>
    p.category.toLowerCase().includes(category.toLowerCase())
  );
}

export function getProductsByTag(tag: string): Product[] {
  return PRODUCT_CATALOG.filter(p =>
    p.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
  );
}

export function getProductsByMomentum(momentum: Momentum): Product[] {
  return PRODUCT_CATALOG.filter(p => p.momentum === momentum);
}

export function getTopProducts(limit = 10): Product[] {
  return [...PRODUCT_CATALOG]
    .sort((a, b) => b.recommendationScore - a.recommendationScore)
    .slice(0, limit);
}
